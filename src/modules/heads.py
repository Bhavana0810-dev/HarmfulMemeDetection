class SelfAttentionHead(nn.Module):
    def __init__(self, config):
        super().__init__()
        self.bertlayer = BertLayer(config)
    
    def forward(self, hidden_states, attention_mask=None):
        outputs, _ = self.bertlayer(hidden_states=hidden_states, attention_mask=attention_mask)
        return outputs


class CrossAttentionHead(nn.Module):
    def __init__(self, config):
        super().__init__()
        config.is_decoder = True
        config.add_cross_attention = True
        self.bertcrosslayer = BertLayer(config)
    
    def forward(
        self,
        hidden_states,
        attention_mask, 
        encoder_hidden_states,
        encoder_attention_mask,
    ):
        outputs, entropy = self.bertcrosslayer(
            hidden_states=hidden_states,
            attention_mask=attention_mask,
            encoder_hidden_states=encoder_hidden_states,
            encoder_attention_mask=encoder_attention_mask,
        )
        return outputs, entropy


class ContrastivePooler(nn.Module):
    def __init__(self, hidden_size):
        super().__init__()
        self.dense = nn.Linear(hidden_size, hidden_size)
        
    def forward(self, hidden_states):
        first_token_tensor = hidden_states[:, 0]
        pooled_output = self.dense(first_token_tensor)
        return pooled_output


class MIMHead(nn.Module):
    def __init__(self, config):
        super().__init__()
        self.decoder = nn.Sequential(
            nn.Conv2d(in_channels=config.hidden_size, out_channels=config.encoder_stride**2 * 3, kernel_size=1),
            nn.PixelShuffle(config.encoder_stride),
        )

    def forward(self, x):
        # cls need to be excluded
        batch_size, sequence_length, num_channels = x.shape
        height = width = int(sequence_length**0.5)
        x = x.permute(0, 2, 1).reshape(batch_size, num_channels, height, width)
        reconstructed_pixel_values = self.decoder(x)
        return reconstructed_pixel_values


class MLMHead(nn.Module):
    def __init__(self, config, weight=None):
        super().__init__()
        self.transform = BertPredictionHeadTransform(config)
        self.decoder = nn.Linear(config.hidden_size, config.vocab_size, bias=False)
        self.bias = nn.Parameter(torch.zeros(config.vocab_size))
        if weight is not None:
            self.decoder.weight = weight

    def forward(self, x):
        x = self.transform(x)
        x = self.decoder(x) + self.bias
        return x


class CAHead(nn.Module):
    def __init__(self):
        super().__init__()
        self.wv = nn.Linear(768, 768)
        self.wk = nn.Linear(768, 768)
        self.wq = nn.Linear(768, 768)
        self.wo = nn.Linear(768, 768)
        self.dropout = nn.Dropout(p=0.1)
    
    def forward(self, query, key, value, attention_masks):
        # query [bsz, 1, 768], key [bsz, seq_len, 768]
        # attention_masks [bsz, seq_len]
        # query, key, value = self.wq(query), self.wk(key), self.wv(value)
        # scores = torch.matmul(query, key.transpose(-1, -2)) # [bsz, 1, seq_len]
        # if attention_masks is not None:
        #     attention_masks = attention_masks.to(dtype=query.dtype).unsqueeze(1) # [bsz, 1, seq_len] fp16 compatibility
        #     attention_masks = (1.0 - attention_masks) * -10000.0
        #     scores = scores + attention_masks
        # scores = torch.softmax(scores, dim=-1)
        # scores = self.dropout(scores)
        # outputs = torch.matmul(scores, value) # [bsz, 1, 768]
        # return self.wo(outputs), scores

        h = 12
        bsz, seq_len, dm = key.size()
        d_h = dm // h
        query, key, value = self.wq(query), self.wk(key), self.wv(value)
        query = query.view(bsz, -1, h, d_h).transpose(1, 2) # [bsz, h, 1, d_h]
        key = key.view(bsz, -1, h, d_h).transpose(1, 2) # [bsz, h, seq_len, d_h]
        value = value.view(bsz, -1, h, d_h).transpose(1, 2) # [bsz, h, seq_len, d_h]

        scores = torch.matmul(query, key.transpose(-1, -2)) # [bsz, h, 1, seq_len]
        if attention_masks is not None:
            attention_masks = attention_masks.to(dtype=query.dtype).unsqueeze(1).unsqueeze(1) # [bsz, 1, 1, seq_len] fp16 compatibility
            attention_masks = (1.0 - attention_masks) * -10000.0
            scores = scores + attention_masks
        scores = torch.softmax(scores, dim=-1)
        scores = self.dropout(scores)
        outputs = torch.matmul(scores, value) # [bsz, h, 1, d_h]
        outputs = outputs.view(bsz, 1, dm)
        return self.wo(outputs), scores


